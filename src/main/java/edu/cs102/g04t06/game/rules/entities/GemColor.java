package edu.cs102.g04t06.game.rules.entities;

/**
 * Represents the different gem colors in the Splendor game.
 * GOLD is a wild card gem obtained by reserving cards.
 */
public enum GemColor {
    /** White gem token. */
    WHITE,
    /** Blue gem token. */
    BLUE,
    /** Green gem token. */
    GREEN,
    /** Red gem token. */
    RED,
    /** Black gem token. */
    BLACK,
    /** Gold joker token. */
    GOLD
}
